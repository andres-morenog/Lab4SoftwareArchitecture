
package com.udea.tablero;

public class Coordinates {
    
    private  float x;
    private float y;
    public Coordinates(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public Coordinates() {
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
    
  
}
